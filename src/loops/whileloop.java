package loops;

public class whileloop {

	public static void main(String[] args) {
	   
	  
	    int i=1;
	    while(i<=10){
	    System.out.println(i);
	    i++;
	    }
	    System.out.println("...............");
	    
	//do-while loop
	int j=1;
	do{
	System.out.println(j);
	j++;
	}while(j<=10);
	
	}
	    
	   
	    
	    
	    
	    
	    
	    }
	


